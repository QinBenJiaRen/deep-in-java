package Problems.Easy.链表;

/**
 * @Description 链表数据结构
 * @Author jia_h
 * @Date 2021/7/30 13:50
 * @Version 1.0
 */
public class ListNode {

    int val;
    ListNode next;
    ListNode(){}
    ListNode(int val) {
        this.val = val;
    }
    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }


}
